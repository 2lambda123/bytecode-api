namespace BytecodeApi.Mathematics
{
	/// <summary>
	/// Specifies the unit in which an angle is measured.
	/// </summary>
	public enum AngleType
	{
		/// <summary>
		/// The angle is measured in radians.
		/// </summary>
		Radians,
		/// <summary>
		/// The angle is measured in degrees.
		/// </summary>
		Degrees,
		/// <summary>
		/// The angle is measured in gradians.
		/// </summary>
		Gradians
	}
}